    private void Next_Room1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Next_Room1ActionPerformed
        // TODO add your handling code here:
        String clientName = Name1.getText();
        String phoneNo = Phone_Number1.getText();
        String age = Age1.getText();
        String duration = Duration1.getText();
        Object roomType = roomTypeCombo1.getSelectedItem();
        String roomType_conv;
        String roomNo = textRoomNo1.getText();

        
        c1.setName(clientName);
        c1.setPhone_number(phoneNo);
        c1.setAge(Integer.parseInt(age));
        c1.setDuration(Integer.parseInt(duration));
        r1.setCapacity(roomType);
        r1.setRoom_id(1);
        r1.setAvailability(false);

        if (roomTypeCombo1.getSelectedIndex() == 0) {
            roomType_conv = "Single";
        } else {
            roomType_conv = "Double";
        }

        Name_L1.setText(c1.getName());
        Phone_Number_L1.setText(c1.getPhone_number());
        Age_L1.setText(c1.getAge() + "");
        Duration_L1.setText(c1.getDuration() + " days");
        Room_Type_L1.setText(roomType_conv);
        Room_L1.setText(roomNo);

        //Calculating price -- 450EGP per day for single and 750EGP for double
        int daysNo = Integer.parseInt(duration);
        int price = 0;

        if (roomType_conv.equals("Single")) {
            price = daysNo * 450;
        } else {
            price = daysNo * 750;
        }
        Price_L1.setText(price + "EGP");

        textRoomNo1.setText("");
        Reception.setVisible(false);
        Login.setVisible(false);
        Infopanel_Room1.setVisible(false);
        Infopanel_Room2.setVisible(false);
        Infopanel_Room3.setVisible(false);
        Infopanel_Room4.setVisible(false);
        Infopanel_Room5.setVisible(false);
        Infopanel_Room6.setVisible(false);
        Infopanel_Room7.setVisible(false);
        Infopanel_Room8.setVisible(false);
        Reportpanel1.setVisible(true);
        Reportpanel2.setVisible(false);
        Reportpanel3.setVisible(false);
        Reportpanel4.setVisible(false);
        Reportpanel5.setVisible(false);
        Reportpanel6.setVisible(false);
        Reportpanel7.setVisible(false);
        Reportpanel8.setVisible(false);
        Roompanel.setVisible(false);
        Foodpanel.setVisible(false);
    }//GEN-LAST:event_Next_Room1ActionPerformed

